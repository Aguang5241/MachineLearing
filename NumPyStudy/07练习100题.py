#########################基础部分################################
#########################1.导入################################
import numpy as np
#########################2.查看版本################################
print(np.__version__)
# 1.18.1
#########################创建数组################################
#########################3.通过列表创建一维数组################################
print(np.array([1, 2, 3]))
# [1 2 3]
#########################4.通过列表创建二维数组################################
print(np.array([[1, 2, 3], [4, 5, 6]]))
# [[1 2 3]
#  [4 5 6]]
#########################5.创建全为0的二维数组################################
print(np.zeros((2, 3)))
# [[0. 0. 0.]
#  [0. 0. 0.]]
#########################6.创建全为1的二维数组################################
print(np.ones((2, 3)))
# [[1. 1. 1.]
#  [1. 1. 1.]]