import numpy as np

##########################1.三角函数#################################
print(np.pi)
# 3.141592653589793
print(np.deg2rad(180))
# 3.141592653589793
print(np.rad2deg(np.pi))
# 180.0
print(np.sin(np.deg2rad(30)))
# 0.49999999999999994
print(np.cos(np.deg2rad(60)))
# 0.5000000000000001
print(np.tan(np.deg2rad(0)))
# 0.0
