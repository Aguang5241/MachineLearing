##########################3.数值修约#################################
a = np.random.randn(5)
print(a)
# [-0.48232468  0.41532893 -0.81063631  0.14404608 -0.73489508]
print(np.around(a))
# [-0.  0. -1.  0. -1.]
print(np.rint(a))
# [-0.  0. -1.  0. -1.]
print(np.fix(a))
# [-0.  0. -0.  0. -0.]
